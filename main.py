if n < k:#проверяем условие
    print("Наименьшее число:",n)#если условие верное ,то выводим ,что n-наименьшее число
elif k < n :#проверяем условие
    print("Наименьшее",k)#если условие верное ,то выводим ,что k-наименьшее число
else :
    print("Числа равны")#если оба условия неверные ,то выводим ,что числа равны